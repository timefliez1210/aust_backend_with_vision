/// Patch `workbook.xml`: fix print area to A:H and force recalculation on load.
///
/// Same approach as the KVA generator: limits PDF output to columns A-H only
/// (avoids printing the helper columns I-P including J50).
fn modify_invoice_workbook(xml: &str) -> String {
    let mut result = xml.to_string();

    // Fix print area: replace dual-range with A:H only
    if let Some(start) = result.find("_xlnm.Print_Area") {
        if let Some(content_start) = result[start..].find('>') {
            let abs_content_start = start + content_start + 1;
            if let Some(end_tag) = result[abs_content_start..].find("</definedName>") {
                let abs_end = abs_content_start + end_tag;
                let mut patched = String::with_capacity(result.len());
                patched.push_str(&result[..abs_content_start]);
                patched.push_str("Tabelle1!$A$1:$H$120");
                patched.push_str(&result[abs_end..]);
                result = patched;
            }
        }
    }

    // Force full recalculation on load
    if result.contains(r#"fullCalcOnLoad="0""#) {
        result = result.replace(r#"fullCalcOnLoad="0""#, r#"fullCalcOnLoad="1""#);
    } else if !result.contains("fullCalcOnLoad=") {
        if let Some(pos) = result.find("<calcPr") {
            let after = &result[pos..];
            if let Some(gt) = after.find("/>") {
                let slash_offset = 1; // point before "/>"
                let insert_at = pos + gt - slash_offset + 1;
                result.insert_str(insert_at, r#" fullCalcOnLoad="true""#);
            }
        }
    }

    result
}

