//! Direct XLSX template manipulation for invoice (Rechnung) generation.
//!
//! Uses the same XML-surgery approach as `xlsx.rs` (offer generator),
//! delegating cell-value writes, row hiding, and formula-cache stripping
//! to the shared functions exported from that module.
//!
//! ## Template
//!
//! The invoice generator uses `templates/Rechnung_Vorlage_v2.xlsx`, which is a
//! copy of `offer_template.xlsx` with only four static label strings patched in
//! `xl/sharedStrings.xml` (via `scripts/make_rechnung_template.py`):
//!
//! | KVA label            | Invoice label     |
//! |----------------------|-------------------|
//! | Kostenvoranschlag    | Rechnung          |
//! | Angebots-Nr.         | Rechnungs-Nr.     |
//! | Angebotsdatum        | Rechnungsdatum    |
//! | Angebot gültig bis   | Zahlungsziel      |
//!
//! All cell layout, styling, logo, formulas, and footer are preserved
//! bit-for-bit from the KVA template.
//!
//! ## Template layout (rows / columns)
//!
//! Invoice template layout (Rechnung_Vorlage_v2.xlsx) — identical to KVA:
//! - **Address block**: A8–A11 (customer), G15 (date)
//! - **Title**: A16 (e.g. "Rechnung Nr. 2026-0042")
//! - **Details**: B17 (Leistungsdatum), B18 (phone / contact), F18 (email)
//! - **Salutation**: A20
//! - **Locations**: A26–A28 (origin), F26–F28 (destination)
//! - **Volume label**: A29
//! - **Line items**: rows 31–46, columns A-G
//!   - A-B (merged): Beschreibung
//!   - C: Bemerkung / Remark
//!   - D: (blank spacer)
//!   - E: Menge (quantity)
//!   - F: Einzelpreis (€/unit, may be negative for credits)
//!   - G: Gesamt Netto (formula `=F*E` or flat value)
//! - **Totals**: G48 = SUM(G31:G46), G49 = 19% MwSt, G51 = Bruttobetrag
//! - **Workers helper**: J50 (kept for formula compatibility; set to 1 for invoices)
//!
//! ## Negative line items (Gutschrift / Anzahlungsabzug)
//!
//! `unit_price` may be negative. The arithmetic propagates correctly through
//! the SUM formula in G48 — no special handling needed. Negative values render
//! with a leading minus sign in the € cell style inherited from the template.

use crate::xlsx::{
    hide_row, set_cell_value, strip_formula_cached_values, unhide_row, CellValue,
};
use crate::OfferError;
use chrono::NaiveDate;
use serde::{Deserialize, Serialize};
use std::io::{Cursor, Read, Write};
use zip::write::SimpleFileOptions;
use zip::{ZipArchive, ZipWriter};

/// The invoice template XLSX — embedded at compile time.
///
/// `Rechnung_Vorlage_v2.xlsx` is generated by `scripts/make_rechnung_template.py`:
/// it copies `offer_template.xlsx` and patches only the four static header
/// label strings in `xl/sharedStrings.xml` (Kostenvoranschlag → Rechnung, etc.).
/// All styling, formulas, logo, and footer are preserved from the KVA template.
const TEMPLATE_BYTES: &[u8] = include_bytes!("../../../templates/Rechnung_Vorlage_v2.xlsx");

/// First row used for line items in the invoice template.
const LINE_ITEM_START_ROW: u32 = 31;
/// Last row used for line items in the invoice template (rows 31–46 = 16 slots).
const LINE_ITEM_END_ROW: u32 = 46;
/// Maximum number of line item rows in the invoice template (rows 31–46).
const MAX_LINE_ITEMS: usize = 16;

// Style indices from the KVA template's xl/styles.xml — reused for invoice.
// These index into the shared styleSheet and are stable across both templates
// because Rechnung_Vorlage_v2.xlsx is derived from offer_template.xlsx.
const STYLES_AB: [&str; 2] = ["58", "53"]; // Description cols A,B: white / blue
const STYLES_C: [&str; 2] = ["59", "54"];  // Remark col C
const STYLES_DE: [&str; 2] = ["60", "55"]; // Quantity cols D,E
const STYLES_F: [&str; 2] = ["61", "56"];  // Unit-price col F (€)
const STYLES_G: [&str; 2] = ["84", "83"];  // Total col G (right-aligned €)

