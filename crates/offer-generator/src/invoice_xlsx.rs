/// Build the list of cell value changes for this invoice, plus row visibility lists.
///
/// Returns `(cell_modifications, hidden_rows, unhidden_rows)`.
///
/// Column layout — identical to KVA template (rows 31-46):
/// - A-B (merged): Beschreibung
/// - C: Bemerkung
/// - D: (blank spacer)
/// - E: Menge
/// - F: Einzelpreis (may be negative for credits)
/// - G: Gesamt Netto (formula `=IF(E="",0,F*E)`)
///
/// Totals block (row numbers reflect KVA template structure):
/// - G48 = SUM(G31:G46) — Nettosumme
/// - G49 = 19% MwSt formula
/// - G51 = Bruttobetrag formula
fn build_cell_modifications(
    data: &InvoiceData,
) -> (Vec<(String, CellValue)>, Vec<u32>, Vec<u32>) {
    let mut mods = Vec::new();

    // Resolve effective address fields (prefer new, fall back to legacy)
    let billing_street = if data.billing_street.is_empty() {
        #[allow(deprecated)]
        data.origin_street.clone()
    } else {
        data.billing_street.clone()
    };
    let billing_city = if data.billing_city.is_empty() {
        #[allow(deprecated)]
        data.origin_city.clone()
    } else {
        data.billing_city.clone()
    };

    // ── Address block ──────────────────────────────────────────────────────
    // For business customers: A8 = company name, A9 = attention line
    // For private customers:  A8 = customer name, A9 = email (or blank)
    if let Some(ref company) = data.company_name {
        mods.push(("A8".into(), CellValue::Text(company.clone())));
        let attn = data
            .attention_line
            .clone()
            .filter(|s| !s.is_empty())
            .or_else(|| data.customer_email.clone())
            .unwrap_or_default();
        mods.push(("A9".into(), CellValue::Text(attn)));
    } else {
        mods.push(("A8".into(), CellValue::Text(data.customer_name.clone())));
        mods.push((
            "A9".into(),
            CellValue::Text(data.customer_email.clone().unwrap_or_default()),
        ));
    }
    mods.push(("A10".into(), CellValue::Text(billing_street.clone())));
    mods.push(("A11".into(), CellValue::Text(billing_city.clone())));

    // ── Date (G15) and clear G14 TODAY() formula ──────────────────────────
    mods.push(("G14".into(), CellValue::Text(String::new())));
    mods.push((
        "G15".into(),
        CellValue::Text(data.invoice_date.format("%d.%m.%Y").to_string()),
    ));

    // ── Title: invoice number (A16) ────────────────────────────────────────
    mods.push((
        "A16".into(),
        CellValue::Text(format!("Rechnung Nr. {}", data.invoice_number)),
    ));

    // ── Leistungsdatum (B17) and email (F18) ──────────────────────────────
    let service_date_str = data
        .service_date
        .map(|d| d.format("%d.%m.%Y").to_string())
        .unwrap_or_default();
    mods.push(("B17".into(), CellValue::Text(service_date_str)));
    if let Some(ref email) = data.customer_email {
        mods.push(("F18".into(), CellValue::StyledText(email.clone(), "0")));
    }

    // ── Salutation / greeting (A20) ────────────────────────────────────────
    mods.push(("A20".into(), CellValue::Text(data.salutation.clone())));

    // ── Job location line (A27) ────────────────────────────────────────────
    // Reuses the A27 cell from the KVA template (origin city row) to show
    // "Auftragsort: <street>, <city>".
    mods.push((
        "A27".into(),
        CellValue::Text(format!(
            "Auftragsort:  {}, {}",
            billing_street, billing_city
        )),
    ));

    // ── Line items (rows 31–46) ────────────────────────────────────────────
    //
    // Style pairs indexed by [color]: 0 = white (fill 4), 1 = blue (fill 3)
    // Style indices are shared with the KVA template (same styles.xml).

    // 1. Hide ALL line-item rows and pre-clear E/F to avoid stale preset values
    let mut hidden_rows: Vec<u32> = (LINE_ITEM_START_ROW..=LINE_ITEM_END_ROW).collect();
    let mut unhidden_rows: Vec<u32> = Vec::new();

    for row in LINE_ITEM_START_ROW..=LINE_ITEM_END_ROW {
        mods.push((format!("E{row}"), CellValue::Number(0.0)));
        mods.push((format!("F{row}"), CellValue::Number(0.0)));
    }

    // 2. Resolve effective line items: prefer new field, fall back to legacy model
    let items_owned: Vec<InvoiceLineItem>;
    #[allow(deprecated)]
    let effective_items: &[InvoiceLineItem] = if !data.line_items.is_empty() {
        &data.line_items
    } else {
        items_owned = build_legacy_line_items(data);
        &items_owned
    };

    if effective_items.len() > MAX_LINE_ITEMS {
        tracing::warn!(
            invoice_number = %data.invoice_number,
            total_items = effective_items.len(),
            max_items = MAX_LINE_ITEMS,
            "Invoice has {} line items but template only has {} slots (rows {}-{}). \
             Excess items will be truncated in PDF.",
            effective_items.len(),
            MAX_LINE_ITEMS,
            LINE_ITEM_START_ROW,
            LINE_ITEM_END_ROW,
        );
    }

    // 3. Write items sequentially, alternating white/blue styles
    for (i, item) in effective_items.iter().take(MAX_LINE_ITEMS).enumerate() {
        let row = LINE_ITEM_START_ROW + i as u32;
        let color = 1 - i % 2; // 1 = blue (first row), 0 = white

        unhidden_rows.push(row);

        // Column A (merged with B): Beschreibung
        mods.push((
            format!("A{row}"),
            CellValue::StyledText(item.description.clone(), STYLES_AB[color]),
        ));
        // Column B (merge partner): keep styled, content ignored
        mods.push((
            format!("B{row}"),
            CellValue::StyledText(String::new(), STYLES_AB[color]),
        ));
        // Column C: Bemerkung / remark
        let remark = item.remark.as_deref().unwrap_or("");
        mods.push((
            format!("C{row}"),
            CellValue::StyledText(remark.to_string(), STYLES_C[color]),
        ));
        // Column D: blank spacer
        mods.push((
            format!("D{row}"),
            CellValue::StyledText(String::new(), STYLES_DE[color]),
        ));
        // Column E: Menge (quantity)
        mods.push((
            format!("E{row}"),
            CellValue::StyledNumber(item.quantity, STYLES_DE[color]),
        ));
        // Column F: Einzelpreis (€, may be negative)
        mods.push((
            format!("F{row}"),
            CellValue::StyledNumber(item.unit_price, STYLES_F[color]),
        ));
        // Column G: Gesamt Netto = F * E  (negative unit_price → negative total)
        let formula = format!("IF(E{row}=\"\",0,F{row}*E{row})");
        mods.push((
            format!("G{row}"),
            CellValue::StyledFormula(formula, STYLES_G[color]),
        ));
    }

    // Remove unhidden rows from hidden list
    hidden_rows.retain(|r| !unhidden_rows.contains(r));

    // 4. Rewrite totals-block SUM to cover all 16 line-item rows (31–46)
    //    G48 = Nettosumme, G49 = 19% MwSt, G51 = Bruttobetrag
    //    Style "72" = right-aligned € total (same as KVA G44 rewrite).
    mods.push((
        "G48".into(),
        CellValue::StyledFormula("SUM(G31:G46)".into(), "72"),
    ));

    // J50 is a helper cell (persons/workers) used by labor formulas in the KVA.
    // For invoices we don't have labor formula rows, but set it to 1 for safety
    // in case the template has any residual references.
    mods.push(("J50".into(), CellValue::Number(1.0)));

    (mods, hidden_rows, unhidden_rows)
}

/// Build line items from the legacy `base_netto_cents` + `extra_services` model.
#[allow(deprecated)]
fn build_legacy_line_items(data: &InvoiceData) -> Vec<InvoiceLineItem> {
    let mut items = Vec::new();

    let base_description = match &data.invoice_type {
        InvoiceType::Full => format!(
            "Umzugsdienstleistung gemäß Angebot Nr. {}",
            data.offer_number
        ),
        InvoiceType::PartialFirst { percent } => format!(
            "Anzahlung ({}%) — Umzugsdienstleistung gemäß Angebot Nr. {}",
            percent, data.offer_number
        ),
        InvoiceType::PartialFinal => format!(
            "Restbetrag — Umzugsdienstleistung gemäß Angebot Nr. {}",
            data.offer_number
        ),
    };
    let base_netto_euros = data.base_netto_cents as f64 / 100.0;
    items.push(InvoiceLineItem {
        pos: 1,
        description: base_description,
        quantity: 1.0,
        unit_price: base_netto_euros,
        remark: None,
    });

    for (i, extra) in data.extra_services.iter().take(15).enumerate() {
        let extra_euros = extra.price_cents as f64 / 100.0;
        items.push(InvoiceLineItem {
            pos: (i + 2) as u32,
            description: extra.description.clone(),
            quantity: 1.0,
            unit_price: extra_euros,
            remark: None,
        });
    }

    items
}

